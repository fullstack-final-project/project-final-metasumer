package com.spring_boot_final.metasumer.model;

public class SafetyTelVO {
	public int telNo;
	public String telSubject;
	public String telContact;
	public String telSite;
	public String telEtc;
	
	public int getTelNo() {
		return telNo;
	}
	public void setTelNo(int telNo) {
		this.telNo = telNo;
	}
	public String getTelSubject() {
		return telSubject;
	}
	public void setTelSubject(String telSubject) {
		this.telSubject = telSubject;
	}
	public String getTelContact() {
		return telContact;
	}
	public void setTelContact(String telContact) {
		this.telContact = telContact;
	}
	public String getTelSite() {
		return telSite;
	}
	public void setTelSite(String telSite) {
		this.telSite = telSite;
	}
	public String getTelEtc() {
		return telEtc;
	}
	public void setTelEtc(String telEtc) {
		this.telEtc = telEtc;
	}
	
	
}
