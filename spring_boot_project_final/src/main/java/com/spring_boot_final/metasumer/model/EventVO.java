package com.spring_boot_final.metasumer.model;

public class EventVO {
	private int prizeNo;
	private String prizeContent;
	private int prizeRank;
	private String prizeCtg;
	private int prizeMonth;
	private int prizeYear;
	private int bizId;
	private int spotId;
	
	public int getPrizeNo() {
		return prizeNo;
	}
	public void setPrizeNo(int prizeNo) {
		this.prizeNo = prizeNo;
	}
	public String getPrizeContent() {
		return prizeContent;
	}
	public void setPrizeContent(String prizeContent) {
		this.prizeContent = prizeContent;
	}
	public int getPrizeRank() {
		return prizeRank;
	}
	public void setPrizeRank(int prizeRank) {
		this.prizeRank = prizeRank;
	}
	public String getPrizeCtg() {
		return prizeCtg;
	}
	public void setPrizeCtg(String prizeCtg) {
		this.prizeCtg = prizeCtg;
	}
	public int getPrizeMonth() {
		return prizeMonth;
	}
	public void setPrizeMonth(int prizeMonth) {
		this.prizeMonth = prizeMonth;
	}
	public int getPrizeYear() {
		return prizeYear;
	}
	public void setPrizeYear(int prizeYear) {
		this.prizeYear = prizeYear;
	}
	public int getBizId() {
		return bizId;
	}
	public void setBizId(int bizId) {
		this.bizId = bizId;
	}
	public int getSpotId() {
		return spotId;
	}
	public void setSpotId(int spotId) {
		this.spotId = spotId;
	}
	
	
}
