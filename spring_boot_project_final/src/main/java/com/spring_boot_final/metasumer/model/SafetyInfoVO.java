package com.spring_boot_final.metasumer.model;

public class SafetyInfoVO {
	public int safetyNo;
	public String safetyTitle;
	public String safetyImg;
	public String safetyBrief;
	
	public int getSafetyNo() {
		return safetyNo;
	}
	public void setSafetyNo(int safetyNo) {
		this.safetyNo = safetyNo;
	}
	public String getSafetyTitle() {
		return safetyTitle;
	}
	public void setSafetyTitle(String safetyTitle) {
		this.safetyTitle = safetyTitle;
	}
	public String getSafetyImg() {
		return safetyImg;
	}
	public void setSafetyImg(String safetyImg) {
		this.safetyImg = safetyImg;
	}
	public String getSafetyBrief() {
		return safetyBrief;
	}
	public void setSafetyBrief(String safetyBrief) {
		this.safetyBrief = safetyBrief;
	}
	
	
}
